import request from '@/utils/request'
export function wechatAuth(data) {
  return request({
    url: '/weixin/webAccountLogin',
    method: 'get',
    params:data
  })
}

export function wechatBind(data) {
  return request({
    url: '/weixin/relateWebOpenidUserid',
    method: 'get',
    params:data
  })
}